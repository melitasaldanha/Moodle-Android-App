package com.moodle;

public class Solution {
    String ques;
    String solution;
    Double marks_obtained;
    Double out_of_marks;

    public String getQues() {
        return ques;
    }

    public void setQues(String ques) {
        this.ques = ques;
    }

    public String getSolution() {
        return solution;
    }

    public void setSolution(String solution) {
        this.solution = solution;
    }

    public Double getMarks_obtained() {
        return marks_obtained;
    }

    public void setMarks_obtained(Double marks_obtained) {
        this.marks_obtained = marks_obtained;
    }

    public Double getOut_of_marks() {
        return out_of_marks;
    }

    public void setOut_of_marks(Double out_of_marks) {
        this.out_of_marks = out_of_marks;
    }
}
